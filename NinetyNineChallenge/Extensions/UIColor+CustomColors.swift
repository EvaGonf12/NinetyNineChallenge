import UIKit

extension UIColor {
    
    @nonobjc class var primary: UIColor {
        return UIColor(red: 130/255, green: 36/255, blue: 227/255, alpha: 1.0)
    }
    
    @nonobjc class var primaryLight: UIColor {
        return UIColor(red: 242/255, green: 240/255, blue: 255/255, alpha: 1.0)
    }
    
    @nonobjc class var light: UIColor {
        return .white
    }
    
    @nonobjc class var dark: UIColor {
        return UIColor(red: 33/255, green: 33/255, blue: 33/255, alpha: 1.0)
    }
    
    @nonobjc class var possitive: UIColor {
        return UIColor(red: 22/255, green: 215/255, blue: 197/255, alpha: 1.0)
    }
    
    @nonobjc class var negative: UIColor {
        return UIColor(red: 228/255, green: 26/255, blue: 145/255, alpha: 1.0)
    }
}

